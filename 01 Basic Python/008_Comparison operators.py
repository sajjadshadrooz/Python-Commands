
# Comparison operators.

number_1 = 30
number_2 = 40

# ==  : return True if the value of number_1 is equal to number_2.
print(f'{ number_1 } == { number_2 } is { number_1 == number_2 }') 

# !=  : return True if the value of number_1 is not equal to number_2.
print(f'{ number_1 } != { number_2 } is { number_1 != number_2 }') 

# >  : return True if the value of number_1 is greater than number_2.
print(f'{ number_1 } > { number_2 } is { number_1 > number_2 }') 

# >=  : return True if the value of number_1 is equal or greater than number_2.
print(f'{ number_1 } >= { number_2 } is { number_1 >= number_2 }') 

# <  : return True if the value of number_1 is less than number_2.
print(f'{ number_1 } < { number_2 } is { number_1 < number_2 }') 

# <=  : return True if the value of number_1 is equal or less than number_2.
print(f'{ number_1 } <= { number_2 } is { number_1 <= number_2 }') 

